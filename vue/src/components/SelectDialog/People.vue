<script>
    import mixin from './mixin'
    // 方法
    // API
    import resourceApi from '@/api/resource'

    export default {
        name: 'People',
        components: {
        },
        mixins: [mixin],
        data() {
            return {
                tableData: {
                    list: [],
                    total: 0,
                    // 通过配置项 更改 table数据列表的展示
                    columns: [
                        {
                            prop: 'userCode',
                            label: '工号',
                            disabled: true
                        },
